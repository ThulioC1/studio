const START_C = '11010011100';
const STOP = '1100011101011';

/**
 * Gera a string binária (barras pretas '1' e brancas '0') de um Code 128 Subgrupo C.
 * @param code A chave de acesso de 44 dígitos.
 */
export function generateBarcode128C(code: string): string {
  const digits = code.replace(/\D/g, '');
  if (digits.length % 2 !== 0) return ''; // Code 128C exige pares

  let binary = START_C;
  let checksum = 105; // Início do checksum com o valor do Start C

  const pairs: number[] = [];
  for (let i = 0; i < digits.length; i += 2) {
    const pair = parseInt(digits.substring(i, i + 2), 10);
    pairs.push(pair);
  }

  pairs.forEach((val, index) => {
    binary += getPattern(val);
    checksum += val * (index + 1);
  });

  const checkDigit = checksum % 103;
  binary += getPattern(checkDigit);
  binary += STOP;

  return binary;
}

